package com.rwanda.health.cds.models;

public class SocialHistory {
    private boolean tobaccoUse;
    private boolean alcoholUse;
    
    // Getters and Setters
    public boolean isTobaccoUse() { return tobaccoUse; }
    public void setTobaccoUse(boolean tobaccoUse) { this.tobaccoUse = tobaccoUse; }
    
    public boolean isAlcoholUse() { return alcoholUse; }
    public void setAlcoholUse(boolean alcoholUse) { this.alcoholUse = alcoholUse; }
}